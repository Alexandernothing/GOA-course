/* 1. მოცემული გაქვთ სია [10, 100, 200, 300, 150, 20, 40, 1000, 150, 125, 900]
და გამოიტანეთ სიიდან ყველაზე დიდი რიცხვი.
*/

let list = [10, 100, 200, 300, 150, 20, 40, 1000, 150, 125, 900]
let max = list[0]

for(let i = 0;i < list.length;i++) {
    if(list[i] > list[max]) {
        max = list[i]
    }
}

